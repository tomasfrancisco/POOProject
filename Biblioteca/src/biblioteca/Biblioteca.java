    private void registarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registarButtonActionPerformed
        String nomeCompleto = nomeCompletoField.getText();
        String email = emailField.getText();
        String reEmail = reEmailField.getText();
        String palavraPasse = new String(palavraPasseRegistoField.getPassword());
        String rePalavraPasse = new String(rePalavraPasseRegistoField.getPassword());
        String dia = diaComboBox.getSelectedItem().toString();
        String mes = mesComboBox.getSelectedItem().toString();
        String ano = anoComboBox.getSelectedItem().toString();
        System.out.println(ano);
        String dataNascimento = dia + " de " + mes + " de " + ano;
        String genero;
        
        if(this.femininoRadioButton.isSelected())
        {
            genero = "Feminino";
        }
        else if(masculinoRadioButton.isSelected())
        {
            genero = "Masculino";
        }            
        else
        {
            avisosRegistoLabel.setText("Verifique o campo g√©nero");
            return;
        }
        
        if(biblioteca.Gestor.verificaEmail(email))
        {
            avisosRegistoLabel.setText("Este e-mail j√° se econtra registado");
            System.out.println("Registo n√£o efectuado com sucesso");
            return;
        }
        else if(nomeCompleto.equals(""))
        {
            avisosRegistoLabel.setText("Verifique o campo Nome Completo");
            System.out.println("Registo n√£o efectuado com sucesso");
            return;
        }
        else if(email.equals(reEmail)==false)
        {
            avisosRegistoLabel.setText("Verifique o campo Email");
            System.out.println("Registo n√£o efectuado com sucesso");
            return;   
        }
        else if(palavraPasse.equals(rePalavraPasse)==false)
        {
            avisosRegistoLabel.setText("Verifique o campo Palavra Passe");
            System.out.println("Registo n√£o efectuado com sucesso");
            return;   
        }
        else if(dia.equals("Dia")||(mes.equals("Mes"))||(ano.equals("Ano")))
        {
            avisosRegistoLabel.setText("Verifique o campo Data");
            System.out.println("Registo n√£o efectuado com sucesso");
            return;   
        }
            
        Gestor.setUtilizador(new Leitor(nomeCompleto, genero, "NULL", "NULL", "NULL", "NULL", email, dataNascimento, "NULL", palavraPasse, Utilizador.ALUNO));
        avisosRegistoLabel.setText("Registo Efectuado com sucesso");
        System.out.println(Gestor.getUtilizadores().size());
        
        nomeCompletoField.setText("");
        emailField.setText("");
        reEmailField.setText("");
        palavraPasseRegistoField.setText("");
        rePalavraPasseRegistoField.setText("");
        diaComboBox.setSelectedIndex(0);
        mesComboBox.setSelectedIndex(0);
        anoComboBox.setSelectedIndex(0);
        generoButtonGroup.clearSelection();
        
        System.out.println("Registo efectuado com sucesso");        
    }//GEN-LAST:event_registarButtonActionPerformed

    private void editarPerfilButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarPerfilButtonActionPerformed
        Utilizador user = Gestor.getUtilizador(usernameField.getText());
        
        editarNomeCompletoField.setText(user.getNome());
        editarEmailField.setText(user.getEmail());
        editarReEmailField.setText("");
        editarPalavraPasseRegistoField.setText(user.getPassword());
        editarRePalavraPasseRegistoField.setText("");
        editarDiaComboBox.setSelectedItem(user.getDataDia());
        editarMesComboBox.setSelectedItem(user.getDataMes());
        editarAnoComboBox.setSelectedItem(user.getDataAno());
        switch (user.getGenero()) {
            case "Masculino":
                editarMasculinoRadioButton.setSelected(true);
                break;
            case "Feminino":
                editarFemininoRadioButton.setSelected(true);
                break;
            default:
                generoButtonGroup.clearSelection();
                break;
        }
        editarMoradaField.setText(user.getMorada());
        editarCodigoPostalField1.setText(user.getCodigo_postalFirst());
        editarCodigoPostalField2.setText(user.getCodigo_postalLast());
        editarDefinirUsernameField.setText(user.getUsername());                
        
        menuLeitor.setVisible(false);
        menuEditarPerfil.setVisible(true);
    }//GEN-LAST:event_editarPerfilButtonActionPerformed

